// use ":" to annotate the type of a name
var message: String
message = "a message"
print(message)

// declare a variable or constant and assign value to it without annotate the type of it is ok
var myName = "Clement"
print(myName)

// annotate the type of multiple variable at once
var red, green, blue: Double
red = 0
green = 1
blue = 2
print(red, green, blue)
